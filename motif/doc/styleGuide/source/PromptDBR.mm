.hR PromptDialog
.hS "Description"
.iX "Prompt DialogBox"
.iX "DialogBox" "Prompt"
A PrompDialog
should be used to prompt the user for input.
It can interrupt the user's interaction with the application.
It should include a message, a text input area, and one
of the following button arrangements.
