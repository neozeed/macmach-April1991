.hR Composition
.hS "Description"
.iX "Composition"
The Composition group should be used to organize components
in an arbitrary layout.  The layout style can be
either arbitrary, in even rows and columns, or so
that the position of components is relative to the
Composition component and the components it contains.
This component is composed of an area for organizing
components.
.hS "Navigation"
.mS
This group must follow the navigation model as described
in Chapter 2.
.mE
.hS "Related Information"
For more information, see the reference pages for
each layout group.
